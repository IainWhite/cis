            <h2>Legacy</h2>
            <p>Netscape's contributions to the web include <?php echo $this->stencil->addLink('JavaScript', 'L'); ?>, which was submitted as a new standard to Ecma International.</p>
            <p>The resultant <?php echo $this->stencil->addLink('ECMAScript', 'JS'); ?> specification allowed JavaScript support by multiple web browsers and its use as a cross-browser scripting language, long after Netscape Navigator itself has dropped in popularity.</p>
            <p>Another example is the <code>FRAME</code> tag, that is widely supported today, and that has been incorporated into official web standards such as the "HTML 4.01 Frameset" specification.</p>
            <p>In a 2007 PC World column, the original Netscape Navigator was considered the "<em>best tech product of all time</em>" due to its impact on the Internet.</p>

            <h2>Versions</h2>

            <h3>Netscape Navigator 1.0 – 3.0</h3>
            <p>Netscape Navigator was the name of Netscape's web browser from versions 1.0 through 4.8.</p>
            <p>The first beta release versions of the browser were released in 1994 and known as Mosaic and then Mosaic Netscape until a legal challenge from the <strong>National Center for Supercomputing Applications</strong> (makers of NCSA <?php echo $this->stencil->addLink('Mosaic', 'WB'); ?>, which many of Netscape's founders used to develop), led to the name change to Netscape Navigator.</p>
            <p>The company's name also changed from Mosaic Communications Corporation to Netscape Communications Corporation.</p>
            <p>The browser was easily the most advanced available and was therefore an instant success, becoming market leader while still in beta.</p>
            <p>Netscape's feature-count and market share continued to grow rapidly after version 1.0 was released.</p>
            <p>Version 2.0 added a full mail reader called Netscape Mail, thus transforming Netscape from a mere web browser to an Internet suite.</p>
            <p>During this period, both the browser and the suite were known as Netscape Navigator.</p>
            <p>Around the same time, AOL started bundling their software with Microsoft's Internet Explorer.</p>
            <p>Version 3.0 of Netscape (the first beta was codenamed "<em>Atlas</em>") was the first to face any serious competition in the form of <?php echo $this->stencil->addLink('Microsoft Internet Explorer 3', 'IE'); ?>.</p>
            <p>Netscape, however, easily held off Microsoft's challenge and remained the number one browser for the time being.</p>
            <p>Version 3.0 was also available in a "Gold" version which featured a WYSIWYG HTML editor (later added to Netscape Communicator as a standard feature).</p>
            <p>Netscape 3.0 introduced many new features such as new plug-ins, background colours for tables, the archive attribute and the applet element.</p>
            <p>Netscape Navigator 3 was a huge success and the undisputed web browser giant in its time with over 90% share, but was later eroded by the free Internet Explorer included with <?php echo $this->stencil->addLink('Windows 98', 'WIN'); ?>.</p>
            <p>The highest version in this line was 3.04.</p>

            <h3>Netscape Communicator 4.0 – 4.8</h3>
            <p>Netscape 4 addressed the problem of Netscape Navigator being used as both the name of the suite and the browser contained within it by renaming the suite to Netscape Communicator.</p>
            <p>After releasing five preview releases from 1996 – 1997, Netscape Corp. released the final version of Netscape Communicator in June 1997.</p>
            <p>This new version, more or less based on Netscape Navigator 3 Code, updated and added new features (such as support of certain CSS1 elements, minimal dynamic font support and the proprietary object element).</p>
            <p>The new suite was successful, despite increasing competition from <?php echo $this->stencil->addLink('Internet Explorer 4', 'IE'); ?> and problems with the outdated browser core.</p>
            <p>The Communicator suite was made up of Netscape Navigator, Netscape Mail and Newsgroups, Netscape Address Book and Netscape Composer (an HTML editor, which later became Mozilla Composer and eventually was split off into a completely separate product, Nvu).</p>
            <p>In October 1998 version 4.5 of Netscape Communicator was released.</p>
            <p>This new version featured various functionality improvements, especially in the Mail/News component, but did not update the browser core (which, in its functionality, was basically identical with version 4.08).</p>
            <p>Only one month later, Netscape Communications Corporation was bought by AOL.</p>
            <p>A standalone version of Netscape Navigator was still available, but this was discontinued after version 4.08 for Windows.</p>
            <p>However, standalone versions for other operating systems such as Unix / Linux were maintained up to version 4.8.</p>
            <p>In January 1998, Netscape Communications Corporation announced that all future versions of their software would be free of charge and developed by an Open Source Community (Mozilla).</p>
            <p>Netscape Communicator 5.0 was announced (codenamed "<em>Gromit</em>"). But there were significant delays to the release of Netscape's next major version and Communicator therefore aged badly over the many years it was still used.</p>
            <p>As a result of this, and a more advanced support of <?php echo $this->stencil->addLink('HTML 4', 'WD'); ?>, <?php echo $this->stencil->addLink('CSS', 'WD'); ?>, <?php echo $this->stencil->addLink('DOM', 'WD'); ?>, and <?php echo $this->stencil->addLink('ECMAScript', 'JS'); ?> by <?php echo $this->stencil->addLink('Internet Explorer', 'WB'); ?> starting with version 4, the more up-to-date <?php echo $this->stencil->addLink('Internet Explorer 5', 'IE'); ?> became the market leader.</p>
            <p>In November 1998 work on Netscape 5.0 was cancelled in favour of developing a completely new program from scratch.</p>

            <h3>Netscape Communicator 5.0 (cancelled)</h3>
            <p>Netscape 5.0 (codenamed "<em>Gromit</em>") was a continuation of the 4.x code, but only two pre-alpha versions were written, one based on original Communicator code (with layout engine stability and performance enhancements to the 4.0 codebase, codenamed <em>Mariner</em>) and another using the <?php echo $this->stencil->addLink('Gecko', 'web-development/layout-engines'); ?> <?php echo $this->stencil->addLink('layout engine', 'WD'); ?>.</p>
            <p>Though Netscape had originally intended to deploy Mariner and later replace it with Gecko (which had been developed from scratch), Netscape 5 and Mariner were scrapped completely.</p>
            <p>Netscape formed the <?php echo $this->stencil->addLink('Mozilla Foundation', 'C'); ?> and develop the <?php echo $this->stencil->addLink('Mozilla Application Suite', 'WB'); ?> using Gecko.</p>
            <p>The open-source Mozilla browser was in turn used as the basis for Netscape 6.</p>

            <h3>Netscape 6.0 – 6.2.3</h3>
            <p>In 1998, an informal group called the Mozilla Organization was formed and largely funded by Netscape (the vast majority of programmers working on the code were paid by Netscape) to co-ordinate the development of Netscape 5 (codenamed "<em>Gromit</em>"), which would be based on the Communicator source code.</p>
            <p>However, the ageing Communicator code proved difficult to work with and the decision was made to scrap Netscape 5 and re-write the source code.</p>
            <p>The re-written source code was in the form of the Mozilla web browser, which, with a few additions, Netscape 6 was based on.</p>
            <p>This decision meant that Netscape's next major version was severely delayed. In the meantime, Netscape was taken over by AOL who, acting under pressure from the Web Standards Project, forced its new division to release Netscape 6.0 in 2000.</p>
            <p>The suite again consisted of Netscape Navigator and the other Communicator components, with the addition of a built-in AOL Instant Messenger client, Netscape Instant Messenger.</p>
            <p>However, it was clear that Netscape 6 was not yet ready for release and it flopped badly.</p>
            <p>It was based on Mozilla 0.6, which was not ready to be used by the general public yet due to many serious bugs that would cause it to crash often or render web pages slowly.</p>
            <p>Later versions of Netscape 6 were much improved (6.2.x was regarded as an especially good release), but the browser still struggled to make an impact on a disappointed community.</p>

            <h3>Netscape 7.0 – 7.2</h3>
            <p>Netscape 7.0 (codenamed "<em>Mach Five</em>" and based on Mozilla 1.0.1) was released in August 2002 was a direct continuation of Netscape 6 with very similar components.</p>
            <p>It picked up a few users, but was still very much a minority browser, one of the problems being that Mozilla itself was a worthy adversary.</p>
            <p>In addition, AOL had decided to deactivate Mozilla's popup-blocker functionality in Netscape 7.0, which created an outrage in the community.</p>
            <p>AOL learned the lesson for Netscape 7.01 and allowed Netscape to reinstate the popup-blocker.</p>
            <p>Netscape also introduced a new AOL free version (without the usual AOL addons) of the browser suite.</p>
            <p>Netscape 7.1 (codenamed "<em>Buffy</em>" and based on Mozilla 1.4) was released in June 2003.</p>
            <p>In 2003, AOL closed down its Netscape division and laid-off or re-assigned all of Netscape's employees.</p>
            <p>Mozilla.org continued, however, as the independent <?php echo $this->stencil->addLink('Mozilla Foundation', 'C'); ?>, taking on many of Netscape's ex-employees.</p>
            <p>AOL continued to develop Netscape in-house, but, due to there being no staff committed to it, improvements were minimal.</p>
            <p>One year later, in August 2004, the last version based on Mozilla was released: Netscape 7.2, based on Mozilla 1.7.2.</p>

            <h3>Netscape Browser 8.0 – 8.1.3</h3>
            <p>Between 2005 and 2007, Netscape's releases became known as Netscape Browser.</p>
            <p>AOL chose to base Netscape Browser on the relatively successful <?php echo $this->stencil->addLink('Mozilla Firefox', 'WB'); ?>, a re-written version of <?php echo $this->stencil->addLink('Mozilla', 'WB'); ?> produced by the <?php echo $this->stencil->addLink('Mozilla Foundation', 'C'); ?>.</p>
            <p>This release is not a full Internet suite as before, but is solely a web browser.</p>
            <p>Other controversial decisions include these versions being made only <?php echo $this->stencil->addLink('Gecko', 'web-development/layout-engines'); ?> rendering engine of previous releases and the <?php echo $this->stencil->addLink('Trident', 'web-development/layout-engines'); ?> engine used in <?php echo $this->stencil->addLink('Internet Explorer', 'WB'); ?>.</p>
            <p>AOL's acquisition of Netscape years ago made it less of a surprise when the company laid off the Netscape team and outsourced development to Mercurial Communications.</p>
            <p>Netscape Browser 8.1.3 was released on 2 April 2007, and included general bug fixes identified in versions 8.0 – 8.1.2</p>

            <h3>Netscape Navigator 9.0</h3>
            <p>Netscape confirmed on 23 January 2007 that Netscape Browser versions 8.0 - 8.1.2 was to be succeeded by a new stand-alone browser release, Netscape Navigator 9.</p>
            <p>It's features were said to include newsfeed support and become more integrated with the Propeller Internet portal, alongside more enhanced methods of discussion, submission and voting on web pages.</p>
            <p>It also sees the browser return to multi-platform support across <?php echo $this->stencil->addLink('Windows', 'OS'); ?>, <?php echo $this->stencil->addLink('Linux', 'OS'); ?> and <?php echo $this->stencil->addLink('Mac OS X', 'OS'); ?>.</p>
            <p>Like Netscape version 8.x, the new release was based upon the popular <?php echo $this->stencil->addLink('Mozilla Firefox', 'WB'); ?> (version 2.0), and had full support of all Firefox add-ons and plugins, some of which Netscape provided.</p>
            <p>Also for the first time since 2004, the browser was produced in-house with its own programming staff.</p>
            <p>A beta of the program was first released on 5 June 2007. The final version was released on 15 October 2007.</p>
            <p>On 28 December 2007, Netscape developers announced that AOL would discontinue their web browser on 1 February 2008, due to low market share.</p>
            <p>On 28 January 2008, Netscape revised this date to 1 March 2008, and offered support for migration to <?php echo $this->stencil->addLink('Flock', 'WB'); ?> and <?php echo $this->stencil->addLink('Mozilla Firefox', 'WB'); ?>.</p>

