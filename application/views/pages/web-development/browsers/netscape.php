        <p><strong>Netscape Navigator</strong> was a proprietary <?php echo $this->stencil->addLink('World Wide Web', 'WD'); ?>.</p>
        <p>It was the flagship product of the <?php echo $this->stencil->addLink('Netscape Communications Corp', 'C'); ?> and was the dominant web browser in terms of usage share in the 1990s, although by 2002 its usage had almost disappeared.</p>
        <p>This was primarily due to the increased usage of Microsoft's <?php echo $this->stencil->addLink('Internet Explorer', 'WN'); ?> web browser software, and partly because the Netscape Corporation (later purchased by <?php echo $this->stencil->addLink('AOL', 'C'); ?>) did not sustain Netscape Navigator's technical innovation after the late 1990s.</p>
        <p>The Netscape Navigator web browser was succeeded by <strong>Netscape Communicator</strong>.<p>
        <p>Netscape Communicator's 4.x source code was the base for the Netscape developed <?php echo $this->stencil->addLink('Mozilla Application Suite', 'WB'); ?>, which was later renamed <?php echo $this->stencil->addLink('SeaMonkey', 'WD'); ?>.</p>
        <p>Netscape's Mozilla Suite also served as the base for a browser-only spinoff called <?php echo $this->stencil->addLink('Mozilla Firefox', 'WB'); ?> and Netscape versions 6 through 9.</p>
        <p>AOL stopped development of Netscape Navigator on 28 December 2007, but continued supporting the web browser with security updates until 1 March 2008.</p>
        <p>Netscape Navigator was based on the <?php echo $this->stencil->addLink('Mosaic', 'WB'); ?> web browser, which was co-written by <strong>Marc Andreessen</strong>, a part-time employee of the <strong>National Center for Supercomputing Applications</strong> (<?php echo $this->stencil->addLink('NCSA', 'C'); ?>) and a student at the University of Illinois.</p>
        <p>After Andreessen graduated in 1993, he moved to California and there met Jim Clark, the recently departed founder of Silicon Graphics.</p>
        <p>Clark believed that the Mosaic browser had great commercial possibilities and provided the seed money.</p>
        <p>Soon <strong>Mosaic Communications Corporation</strong> was in business in Mountain View, California, with Andreessen as a vice-president.</p>
        <p>Since the University of Illinois was unhappy with the company's use of the Mosaic name, the company changed its name to <strong>Netscape Communications</strong> (thought up by Product Manager Greg Sands) and named its flagship web browser Netscape Navigator.</p>
