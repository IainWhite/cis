        <p>Lean manufacturing is a management philosophy focusing on reduction of the 7 wastes (Over-production, Waiting time, Transportation, Processing, Inventory, Motion and Scrap) in manufactured products.</p>
        <p>These principles have revolutionized manufacturing and have been adopted by the most innovative product companies including Toyota and 3M.</p>
        <p>In 2003 the Poppendieck's published <strong><?php echo $this->stencil->addLink('Lean Software Development: An Agile Toolkit', 'B'); ?></strong> which showed how these same lean principles can be successfully applied to software development.</p>
        <p>Since that publication the authors have increased their understanding of <?php echo $this->stencil->addLink('Lean', 'PM'); ?>  and <?php echo $this->stencil->addLink('Agile', 'PM'); ?> problems faced by large organizations and have emerged as leading advocates for bringing Lean production techniques to software development.</p>
        <p>While their first book provides an introduction, theoretical advice and a reference to Lean, this follow-up incorporates their gained knowledge and understanding of what works and goes steps further to provide hands-on guidance for implementing a Lean system.</p>
        <p>Using historical case studies from prominent companies such as Polaris, Lockheed and Fujistu the authors prove the overall value of Lean practices and shows how to effectively apply these methods to software production.</p>

