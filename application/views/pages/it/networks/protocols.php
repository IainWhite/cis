        <p>In telecommunications, a communications protocol is a system of digital rules for data exchange within or between computers.</p>
        <p>Communicating systems use well-defined formats for exchanging messages.</p>
        <p>Each message has an exact meaning intended to elicit a response from a range of possible responses pre-determined for that particular situation.</p>
        <p>Thus, a protocol must define the syntax, semantics, and synchronization of communication; the specified behaviour is typically independent of how it is to be implemented.</p>
        <p>A protocol can therefore be implemented as hardware, software, or both.</p>
        <p>Communications protocols have to be agreed upon by the parties involved.</p>
        <p>To reach agreement a protocol may be developed into a technical standard.</p>
