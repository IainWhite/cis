        <p>The <strong>physical layer</strong> has the following major functions:</p>
        <ul>
            <li>It defines the electrical and physical specifications of the data connection. It defines the relationship between a device and a physical transmission medium (e.g., a copper or fiber optical cable). This includes the layout of pins, voltages, line impedance, cable specifications, signal timing, hubs, repeaters, network adapters, host bus adapters (HBA used in storage area networks) and more.</li>
            <li>It defines the protocol to establish and terminate a connection between two directly connected nodes over a communications medium.</li>
            <li>It may define the protocol for flow control.</li>
            <li>It defines transmission mode i.e. simplex, half duplex, full duplex.</li>
            <li>It defines topology.</li>
        </ul>
        <p>The physical layer of Parallel SCSI operates in this layer, as do the physical layers of Ethernet and other local-area networks, such as Token Ring, FDDI, ITU-T G.hn, and IEEE 802.11 (Wi-Fi), as well as personal area networks such as Bluetooth and IEEE 802.15.4.</p>
