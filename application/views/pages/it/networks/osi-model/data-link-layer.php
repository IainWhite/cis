        <p>The <strong>data link layer</strong> provides node-to-node data transfer - a reliable link between two directly connected nodes, by detecting and possibly correcting errors that may occur in the physical layer. The data link layer is divided into two sublayers:</p>
        <ul>
            <li>Media Access Control (MAC) layer - responsible for controlling how devices in a network gain access to data and permission to transmit it.</li>
            <li>Logical Link Control (LLC) layer - controls error checking and packet synchronization.</li>
            <li>The Point-to-Point Protocol (PPP) is an example of a data link layer in the TCP/IP protocol stack.</li>
        </ul>
        <p>The ITU-T G.hn standard, which provides high-speed local area networking over existing wires (power lines, phone lines and coaxial cables), includes a complete data link layer that provides both error correction and flow control by means of a selective repeat sliding window protocol.</p>
