        <p>In computer programming, <strong>C</strong>reate, <strong>R</strong>ead, <strong>U</strong>pdate and <strong>D</strong>elete (as an acronym <strong>CRUD</strong>) are the four basic functions of persistent storage.</p>
        <p>Sometimes CRUD is expanded with the words retrieve instead of read, modify instead of update, or destroy instead of delete.</p>
        <p>It is also sometimes used to describe user interface conventions that facilitate viewing, searching, and changing information; often using computer-based forms and reports.</p>
        <p>The term was likely first popularized by <strong>James Martin</strong> in his 1983 book <strong><em>Managing the Data-base Environment</em></strong>.</p>
        <p>Another variation of CRUD is <strong>BREAD</strong>, an acronym for "<strong>B</strong>rowse, <strong>R</strong>ead, <strong>E</strong>dit, <strong>A</strong>dd, <strong>D</strong>elete".</p>

