        <?php echo $this->stencil->setHeadline('MooTools JavaScript Framework'); ?>
        <p><strong>MooTools (My Object-Oriented Tools)</strong> is a lightweight, object-oriented, <?php echo $this->stencil->addLink('JavaScript', 'L'); ?> framework. It is released under the free, open-source MIT License.</p>
        <p><strong>Valerio Proietti</strong> first authored the framework and released it in September 2006 taking as his inspiration <?php echo $this->stencil->addLink('Prototype', 'JSF'); ?> and <?php echo $this->stencil->addLink('Dean Edward', 'P'); ?>'s <?php echo $this->stencil->addLink('base2', 'JSF'); ?>. MooTools originated from <strong>Moo.fx</strong>, a popular plug-in Proietti produced for Prototype in October 2005, which is still maintained and used.</p>
        <p>Whereas Prototype extended—prototyped—many of JavaScript's native String, Array, and Function objects with additional methods, Proietti desired a framework that (at the time) further extended the native Element object as well to offer greater control of the Document Object Model (DOM).</p>

        <h2>Components</h2>
        <p>MooTools includes a number of components, but not all need to be loaded for each application. Some of the component categories are:</p>
        <ul>
            <li>Core: A collection of utility functions that all the other components require.</li>
            More: An official collection of add-ons that extend the Core and provide enhanced functionality.</li>
            Class: The base library for Class object instantiation.</li>
            Natives: A collection of JavaScript Native Object enhancements. The Natives add functionality, compatibility, and new methods that simplify coding.</li>
            Element: Contains a large number of enhancements and compatibility standardization to the HTML Element object.</li>
            Fx: An advanced effects-API to animate page elements.</li>
            Request: Includes XHR interface, Cookie, JSON, and HTML retrieval-specific tools for developers to exploit.</li>
            Window: Provides a cross-browser interface to client-specific information, such as the dimensions of the window.</li>
        </ul>
