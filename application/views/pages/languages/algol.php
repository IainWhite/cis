        <p><strong>ALGOL 60</strong> (short for <strong>ALGO</strong>rithmic <strong>L</strong>anguage 19<strong>60</strong>) is a member of the ALGOL family of computer programming languages.</p>
        <p>It followed on from ALGOL 58 which had introduced code blocks and the begin and end pairs for delimiting them.</p>
        <p>ALGOL 60 was the first language implementing nested function definitions with lexical scope.</p>
        <p>It gave rise to many other programming languages, including <?php echo $this->stencil->addLink('BCPL', 'L'); ?>, B, <?php echo $this->stencil->addLink('Pascal', 'L'); ?>, Simula and <?php echo $this->stencil->addLink('C', 'L'); ?>.</p>
        <p>Niklaus Wirth based his own ALGOL W on ALGOL 60 before moving to develop Pascal.</p>
        <p>Algol-W was intended to be the next generation ALGOL but the ALGOL 68 committee decided on a design that was more complex and advanced rather than a cleaned simplified ALGOL 60.</p>
        <p>The official ALGOL versions are named after the year they were first published.</p>
        <p>Algol 68 is substantially different from Algol 60 and was criticised partially for being so, so that in general "Algol" refers to dialects of Algol 60.</p>
