        <p><strong>Erlang</strong> is a general-purpose concurrent, garbage-collected programming language and runtime system.</p>
        <p>The sequential subset of Erlang is a functional language, with eager evaluation, single assignment, and dynamic typing.</p>
        <p>It was designed by Ericsson to support distributed, fault-tolerant, soft-real-time, non-stop applications.</p>
        <p>It supports hot swapping, so that code can be changed without stopping a system.</p>
        <p>While threads require external library support in most languages, Erlang provides language-level features for creating and managing processes with the aim of simplifying concurrent programming.</p>
        <p>Though all concurrency is explicit in Erlang, processes communicate using message passing instead of shared variables, which removes the need for explicit locks (a locking scheme is still used internally by the VM).</p>
        <p>The first version was developed by Joe Armstrong in 1986.</p>
        <p>It was originally a proprietary language within Ericsson, but was released as open source in 1998.</p>
