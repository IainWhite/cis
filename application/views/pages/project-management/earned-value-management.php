        <p><strong>Earned Value Management (EVM)</strong>, or <strong>Earned Value Project / Performance Management (EVPM)</strong> is a project management technique for measuring project performance and progress in an objective manner.</p>
        <p>Earned value management is a project management technique for measuring project performance and progress. It has the ability to combine measurements of the project management triangle:</p>
        <ul>
            <li>Scope</li>
            <li>Schedule, and</li>
            <li>Costs</li>
        </ul>

        <p>In a single integrated system, Earned Value Management is able to provide accurate forecasts of project performance problems, which is an important contribution for project management.</p>
        <p>Early EVM research showed that the areas of planning and control are significantly impacted by its use; and similarly, using the methodology improves both scope definition as well as the analysis of overall project performance. More recent research studies have shown that the principles of EVM are positive predictors of project success. Popularity of EVM has grown in recent years beyond government contracting, in which sector its importance continues to rise in part because EVM can also surface in and help substantiate contract disputes.</p>
        <p>Essential features of any EVM implementation include:</p>
        <ul>
            <li>a project plan that identifies work to be accomplished,</li>
            <li>a valuation of planned work, called Planned Value (PV) or Budgeted Cost of Work Scheduled (BCWS), and</li>
            <li>pre-defined “earning rules” (also called metrics) to quantify the accomplishment of work, called Earned Value (EV) or Budgeted Cost of Work Performed (BCWP).</li>
        </ul>

        <p>EVM implementations for large or complex projects include many more features, such as indicators and forecasts of cost performance (over budget or under budget) and schedule performance (behind schedule or ahead of schedule). However, the most basic requirement of an EVM system is that it quantifies progress using PV and EV.</p>
