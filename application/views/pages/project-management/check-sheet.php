        <?php echo $this->stencil->setHeadline('Check Sheet'); ?>
        <p>The <strong>check sheet</strong> is a form (document) used to collect data in real time at the location where the data is generated. The data it captures can be quantitative or qualitative. When the information is quantitative, the check sheet is sometimes called a tally sheet.</p>
        <p>The check sheet is one of the so called <?php echo $this->stencil->addLink('Seven Basic Tools of Quality Control', 'PM'); ?>.</p>

        <p>The defining characteristic of a check sheet is that data are recorded by making marks ("checks") on it. A typical check sheet is divided into regions, and marks made in different regions have different significance. Data are read by observing the location and number of marks on the sheet.</p>
        <p>Check sheets typically employ a heading that answers the Five Ws:</p>
        <ul>
            <li>Who filled out the check sheet</li>
            <li>What was collected (what each check represents, an identifying batch or lot number)</li>
            <li>Where the collection took place (facility, room, apparatus)</li>
            <li>When the collection took place (hour, shift, day of the week)</li>
            <li>Why the data were collected</li>
        </ul>

        <p>Kaoru Ishikawa identified five uses for check sheets in quality control:</p>
        <ul>
            <li>To check the shape of the probability distribution of a process</li>
            <li>To quantify defects by type</li>
            <li>To quantify defects by location</li>
            <li>To quantify defects by cause (machine, worker)</li>
            <li>To keep track of the completion of steps in a multi-step procedure</li>
        </ul>
