        <p><strong>Crystal Clear</strong> is a member of the Crystal family of methodologies as described by <strong>Alistair Cockburn</strong> and is considered an example of an <?php echo $this->stencil->addLink('agile', 'PM'); ?> or lightweight methodology.</p>
        <p>Crystal Clear can be applied to teams of up to 6 or 8 co-located developers working on systems that are not life critical. The Crystal family of methodologies focus on efficiency and habitability as components of project safety. Crystal Clear focuses on people, not processes or artefacts.</p>
        <p>Crystal Clear requires the following properties:</p>
        <ul>
            <li>Frequent delivery of usable code to users</li>
            <li>Reflective improvement</li>
            <li>Osmotic communication preferably by being co-located</li>
        </ul>
        <p>Crystal Clear additionally includes these optional properties:</p>
        <ul>
            <li>Personal safety</li>
            <li>Focus</li>
            <li>Easy access to expert users</li>
            <li>Automated tests, configuration management, and frequent integration</li>
        </ul>
