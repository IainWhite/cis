        <p><strong>Fishbone diagrams</strong> (also called as <strong>herringbone diagrams</strong>, <strong>Ishikawa diagrams</strong>, <strong>cause and effect diagrams</strong>, or <strong>Fishikawa</strong>) are causal diagrams created by <strong>Kaoru Ishikawa</strong> (1968) that show the causes of a specific event. Common uses of the Ishikawa diagram are product design and quality defect prevention, to identify potential factors causing an overall effect. Each cause or reason for imperfection is a source of variation. Causes are usually grouped into major categories to identify these sources of variation. The categories typically include:</p>
        <ul>
            <li>People: Anyone involved with the process</li>
            <li>Methods: How the process is performed and the specific requirements for doing it, such as policies, procedures, rules, regulations and laws</li>
            <li>Machines: Any equipment, computers, tools, etc. required to accomplish the job</li>
            <li>Materials: Raw materials, parts, pens, paper, etc. used to produce the final product</li>
            <li>Measurements: Data generated from the process that are used to evaluate its quality</li>
            <li>Environment: The conditions, such as location, time, temperature, and culture in which the process operates</li>
        </ul>
