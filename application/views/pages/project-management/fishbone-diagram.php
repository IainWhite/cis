        <p>Ishikawa diagrams were popularized in the 1960s by Kaoru Ishikawa, who pioneered quality management processes in the Kawasaki shipyards, and in the process became one of the founding fathers of modern management.</p>
        <p>The basic concept was first used in the 1920s, and is considered one of the <?php echo $this->stencil->addLink('seven basic tools of quality control', 'PM'); ?>. It is known as a fishbone diagram because of its shape, similar to the side view of a fish skeleton.</p>

        <h2>Causes</h2>
        <p>Causes in the diagram are often categorized, such as to the 5 M's, described below. Cause and effect diagrams can reveal key relationships among various variables, and the possible causes provide additional insight into process behavior.</p>
        <p>Typical categories areCauses can be derived from brainstorming sessions. These groups can then be labeled as categories of the fishbone. They will typically be one of the traditional categories mentioned above but may be something unique to the application in a specific case. Causes can be traced back to root causes with the <?php echo $this->stencil->addLink('5 Whys', 'PM'); ?> technique.</p>

        <p>Typical categories are:</p>

        <h3>The 5 M's (used in manufacturing industry)</h3>
        <ul>
            <li>Machine (technology)</li>
            <li>Method (process)</li>
            <li>Material (Includes Raw Material, Consumables and Information.)</li>
            <li>Man Power (physical work)/Mind Power (brain work)</li>
            <li>Measurement (Inspection)</li>
        </ul>
        <p>The original 5 M's used by the <strong>Toyota Production System</strong> have been expanded by some to include the following and are referred to as the 8 Ms. However, this is not globally recognized. It has been suggested to return to the roots of the tools and to keep the teaching simple while recognizing the original intent; most programs do not address the 8M's.</p>
        <ul>
            <li>Milieu / Mother Nature (Environment)</li>
            <li>Management / Money Power</li>
            <li>Maintenance</li>
        </ul>

        <h3>The 7 P's (used in marketing industry)</h3>
        <ul>
            <li>Product / Service</li>
            <li>Price</li>
            <li>Place</li>
            <li>Promotion</li>
            <li>People / personnel</li>
            <li>Process</li>
            <li>Physical Evidence</li>
        </ul>

        <h3>The 5 S's (used in service industry)</h3>
        <ul>
            <li>Surroundings</li>
            <li>Suppliers</li>
            <li>Systems</li>
            <li>Skills</li>
            <li>Safety</li>
        </ul>
