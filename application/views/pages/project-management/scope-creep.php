        <p><strong>Scope creep</strong> (also called <strong>requirement creep</strong> and <?php echo $this->stencil->addLink('feature creep', 'PM'); ?>) in project management refers to uncontrolled changes or continuous growth in a project’s scope. This can occur when the scope of a project is not properly defined, documented, or controlled. It is generally considered harmful.</p>
        <p>If budget, resources, and schedule are increased along with the scope, the change is usually considered an acceptable addition to the project, and the term “scope creep” is not used.</p>
        <p>Scope creep can be a result of:</p>
        <ul>
            <li>poor change control</li>
            <li>lack of proper initial identification of what is required to bring about the project objectives</li>
            <li>weak project manager or executive sponsor</li>
            <li>poor communication between parties</li>
            <li>lack of initial product versatility</li>
        </ul>
        <p>Scope creep is a risk in most projects. Scope creep often results in <?php echo $this->stencil->addLink('cost overrun', 'PM'); ?>. A value for free strategy is difficult to counteract and remains a difficult challenge for even the most experienced project managers.</p>
