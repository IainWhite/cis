        <p><strong>ISO 27001</strong> (formally known as <strong>ISO/IEC 27001:2005</strong>) is a specification for an Information Security Management System (ISMS). An ISMS is a framework of policies and procedures that includes all legal, physical and technical controls involved in an organisation's information risk management processes.</p>
        <p>According to its documentation, ISO 27001 was developed to "provide a model for establishing, implementing, operating, monitoring, reviewing, maintaining and improving an information security management system".
        <p>ISO 27001 uses a topdown, risk-based approach and is technology neutral. The specification defines a six part planning process:
        <ol>
            <li>Define a security policy.</li>
            <li>Define the scope of the ISMS.</li>
            <li>Conduct a risk assessment.</li>
            <li>Manage identified risks.</li>
            <li>Select control objectives and controls to be implemented.</li>
            <li>Prepare a statement of applicability.</li>
        </ol>
        <p>The specification includes details for documentation, management responsibility, internal audits, continual improvement, and corrective and preventive action. The standard requires cooperation among all sections of an organisation.</p>
        <p>The 27001 standard does not mandate specific information security controls, but it provides a checklist of controls that should be considered in the accompanying code of practice, ISO/IEC 27002:2005. This second standard describes a comprehensive set of information security control objectives and a set of generally accepted good practice security controls.</p>

        <p>ISO 27002 contains 12 main sections:</p>
        <ol>
            <li>Risk assessment</li>
            <li>Security policy</li>
            <li>Organization of information security</li>
            <li>Asset management</li>
            <li>Human resources security</li>
            <li>Physical and environmental security</li>
            <li>Communications and operations management</li>
            <li>Access control</li>
            <li>Information systems acquisition, development and maintenance</li>
            <li>Information security incident management</li>
            <li>Business continuity management</li>
            <li>Compliance</li>
        </ol>

        <p>Organisations are required to apply these controls appropriately in line with their specific risks. Third party accredited certification is recommended for ISO 27001 conformance.</p>
        <p>Other standards being developed in the 27000 family are:</p>
        <ul>
            <li>27003 - implementation guidance.</li>
            <li>27004 - an information security management measurement standard suggesting metrics to help improve the effectiveness of an ISMS.</li>
            <li>27005 - an information security risk management standard. (Published in 2008)</li>
            <li>27006 - a guide to the certification or registration process for accredited ISMS certification or registration bodies. (Published in 2007)</li>
            <li>27007 - ISMS auditing guideline.
        </ul>

