        <h2>Certifications</h2>
        <p></p>The <strong>RegPM ("Registered Project Manager")</strong> is AIPM's project management certification program. Candidates who undertake the program are required to compile and show evidence that displays their competence in Project Management methodologies. The award is given at one of four levels.</p>
        <ul>
            <li>CPPE - Certified Practising Portfolio Executive</li>
            <li>CPPD - Certified Practising Project Director</li>
            <li>CPPM - Certified Practising Project Manager</li>
            <li>CPPP - Certified Practising Project Practitioner</li>
        </ul>

        <h2>Competence Based Assessment</h2>
        <p>The candidate undertaking the RegPM program must fully understand, and be competent in, all components of a project, program or Portfolio life-cycle. They must show this based around their current and previous workplace experiences. Accreditation or certification for the levels requires the completion of an <strong>Assessment Record Book (ARB)</strong>. The Completion of the ARB consists of answering many questions on Project, Program or Portfolio Management theory which also requires the candidate to provide a logbook of evidence supporting their level of competence.</p>
        <p>Candidates of the RegPM award are assigned an assessor and must show they have achieved a level of competence in the following areas of Project Management:</p>
        <ul>
            <li>Plan and Manage Scope</li>
            <li>Plan and Manage Time</li>
            <li>Manage Project Costs</li>
            <li>Plan and manage quality</li>
            <li>Plan and manage Project Human Resources</li>
            <li>Plan and manage project communications</li>
            <li>Plan and manage project risk</li>
            <li>Plan and manage project procurement</li>
            <li>Manage Application of Project Integrative Processes</li>
        </ul>
