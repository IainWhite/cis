        <p>Acornsoft was formed in late 1980 by Acorn Computers directors <strong>Hermann Hauser</strong> and <strong>Chris Curry</strong>, and <strong>David Johnson-Davies</strong>, author of the first game for a UK personal computer and of the official Acorn Atom manual "<em>Atomic Theory and Practice</em>".</p>
        <p>David Johnson-Davies was Managing Director and in early 1981 was joined by Tim Dobson, Programmer and Chris Jordan, Publications Editor.</p>
        <p>While some of their games were clones or remakes of popular arcade games (e.g. <strong>Hopper</strong> is a clone of <strong>Sega's Frogger</strong>, <strong>Snapper is <strong>Namco's Pac-Man</strong>, <strong>Arcadians</strong> is <strong>Namco's Galaxian</strong>), they also published a number of original ground-breaking titles such as <strong>Aviator</strong>, <strong>Elite</strong> and <strong>Revs</strong> which went on to spawn entire genres that live on to this day.</p>
        <p>Acornsoft also published a number of text adventure games by authors such as <strong>Peter Killworth</strong>, including <strong>Philosopher's Quest</strong> (previously titled Brand X) and <strong>Countdown to Doom</strong>, that remain highly regarded within the interactive fiction community.</p>
        <p>Acornsoft ceased to operate as a separate company upon the departure of David Johnson-Davies in January 1986.</p>
